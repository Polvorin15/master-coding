// COMANDOS BÁSICOS DE LA TERMINAL
// pwd -> muestra la ruta en donde nos encontramos
// ls -> muestra qué hay dentro de la ruta donde estoy ("ls -l" enlista, "ls -a" muestra todos)
// ls -la -> muestra todo lo que hay dentro de la ruta en forma enlistada
// cd -> cambio de carpeta ("cd nombre-carpeta" o "cd .." nos regresa a una carpeta anterior)
// touch -> crea un archivo ("touch nombre-archivo")
// mkdir -> crea carpeta ("mkdir nombre-carpeta")
// nano -> editor de texto dentro de la terminal ("nano archivo")
// cat -> nos muestra que contenido tiene un archivo ("cat archivo")
// mv -> mover un archivo de lugar o cambiarle el nombre ("mv archivo nueva-locacion" o "mv archivo nuevo-nombre")
// rm -> borra archivo o carpeta ("rm archivo" o "rm -r carpeta")
// rm -rf -> borra todo (MUCHO CUIDADO CON ESTE COMANDO)
// clear -> limpia la pantalla
